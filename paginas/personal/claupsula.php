<p>7.6.  Si el TRABAJADOR realiza viajes al interior y al exterior para la realización de sus tareas, el mismo debe respetar las instrucciones que en ese sentido imparta la empresa, siendo los gastos de traslado y viático por cuenta de ésta.  El incumplimiento de esta disposición será causal de despido con causa justificada.
